package tiles;

public class MapTransition {
	// Classe auxiliaire pour gérer les transitions de carte
	    String mapPath;
	    int startX, startY;

	    public MapTransition(String mapPath, int startX, int startY) {
	        this.mapPath = mapPath;
	        this.startX = startX;
	        this.startY = startY;
	    }

}
